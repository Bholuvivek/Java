// Bitwise XOR (^)
// a = 5 = 0101 (In Binary)
// b = 7 = 0111 (In Binary)
//
//Bitwise XOR Operation of 5 and 7
//  0101
//^ 0111
// ________
//  0010  = 2 (In decimal)
public class BitwiseXORDemo {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        System.out.println("a^b = " + (a ^ b));
    }

}
