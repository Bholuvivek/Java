// Bitwise OR (|)
// a = 7 = 0111 (In Binary)
// b = 7 = 0111 (In Binary)
//
// Bitwise OR Operation of 7 and 7
//  0111
//  0111
// ________
//  0111  = 7(In decimal)
public class BitwiseORDemo {
    public static void main(String[] args) {
        int a = 7;
        int b = 7;
        System.out.println("a|b = " + (a | b));
    }

}
