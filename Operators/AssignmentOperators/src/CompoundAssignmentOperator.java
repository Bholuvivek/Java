public class CompoundAssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        a +=10;
        System.out.println(a);
        a -=10;
        System.out.println(a);
        a *=10;
        System.out.println(a);
        a /=10;
        System.out.println(a);


    }
}
