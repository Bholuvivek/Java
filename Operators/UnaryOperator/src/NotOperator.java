// Not Operator(!):
// This Operator use to true to false and false to true with the help (!)
public class NotOperator {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println(a);
        System.out.println(!a);
        System.out.println(b);
        System.out.println(!b);
    }
}
