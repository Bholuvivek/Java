// It is used to decrement the value of an integer.
// It Also Implement in two ways:
// 1. Post-decrement operator
// 2. Pre-decrement operator
public class Decrement {
    public static void main(String[] args) {
        int a = 9;
        System.out.println(a); // OutPut = 9
        a--;
        System.out.println(a); // OutPut = 8
        --a;
        System.out.println(a); // OutPut = 7
        System.out.println(a--); // OutPut = 7
    }
}
