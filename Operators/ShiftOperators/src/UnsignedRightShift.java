//3. Unsigned Right Shift Operator " >>> "
// Syntax:
//
//left_operand  >>>  number
public class UnsignedRightShift {
    public static void main(String[] args) {

        byte num1 = 8;
        byte num2 = -8;

        System.out.println(num1 >>> 2);
        System.out.println(num2 >>> 2);
    }
}
